from paddleocr import PaddleOCR

# =============================================================================
# PaddleOCR 모델 초기화
#
# 한국어 인식에 최적화된 설정을 사용하여 OCR 엔진을 로드합니다.
# 처음 실행 시 모델 파일을 자동으로 다운로드하므로 시간이 조금 걸릴 수 있습니다.
# =============================================================================
ocr = PaddleOCR(
    # 한국어 텍스트 인식 모델 사용 (korean_PP-OCRv5_mobile_rec)
    text_recognition_model_name="korean_PP-OCRv5_mobile_rec",
    
    # 문서 방향 분류(0도, 90도, 180도 등) 모델 사용 여부
    # False로 설정하여 불필요한 연산을 줄이고 속도를 높입니다. (이미지가 정방향이라고 가정)
    use_doc_orientation_classify=False,
    
    # 문서가 구겨지거나 왜곡된 것을 펴주는 모듈 사용 여부
    # 일반적인 평면 이미지라면 False로 설정합니다.
    use_doc_unwarping=False,
    
    # 텍스트 라인의 방향(가로/세로)을 감지할지 여부
    # True로 설정하여 세로 쓰기 텍스트도 잘 인식하도록 합니다.
    use_textline_orientation=True,
    
    # 실행할 장치 설정 (gpu:0 또는 cpu)
    # GPU가 없다면 자동으로 CPU 모드로 동작하지만, 명시적으로 'cpu'로 설정할 수도 있습니다.
    device="gpu:0", 
)

# =============================================================================
# 이미지에서 텍스트 인식 수행
#
# 지정된 이미지 파일 경로를 입력받아 OCR을 수행합니다.
# result 변수에는 인식된 텍스트 박스 위치, 텍스트 내용, 신뢰도가 포함됩니다.
# =============================================================================
img_path = "../tests/fixtures/초코파이_detail.jpg" # 테스트할 이미지 경로 (text_processors 폴더 기준)
print(f"이미지 인식 시작: {img_path}")
result = ocr.predict(img_path)

# =============================================================================
# 결과 출력 및 저장
# =============================================================================
for res in result:
    # 1. 콘솔에 결과 출력
    # (텍스트 박스 좌표, 텍스트, 신뢰도 순으로 출력됩니다)
    res.print()
    
    # 2. 결과 이미지 저장 (output 폴더)
    # 원본 이미지 위에 인식된 텍스트 박스와 텍스트를 그려서 저장합니다.
    # 파일명은 'vis_{원본파일명}.jpg' 형태로 저장됩니다.
    res.save_to_img("output")
    print("결과 이미지 저장 완료: output/vis_초코파이_detail.jpg")              
    
    # 3. 결과 JSON 저장 (output 폴더)
    # 상세 인식 정보를 JSON 파일로 저장합니다.
    # 이 파일은 추후 LLM 요약 단계(ocr_llm_summarizer.py)의 입력으로 사용됩니다.
    res.save_to_json("output")
    print("결과 JSON 저장 완료: output/초코파이_detail_res.json")  
